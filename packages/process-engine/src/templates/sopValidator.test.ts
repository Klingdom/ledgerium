// ─── Rule 5 (bonus): generic_title ───────────────────────────────────────────

describe('validateRenderedSOP — Rule 5: generic_title', () => {
  const GENERIC_TITLES = [
    'Workflow 1',
    'Untitled Process',
    'workflow',
    'Untitled Workflow 42',
    'Workflow',
    'WORKFLOW 12',
  ];

  for (const title of GENERIC_TITLES) {
    it(`rejects generic title "${title}"`, () => {
      const rendered = validRenderedSOP({ taskTitle: title });
      const result = validateRenderedSOP(rendered, validOutput());

      expect(result.ok).toBe(false);
      if (!result.ok) {
        expect(result.reason).toBe('generic_title');
        expect(result.diagnostic).toContain(title);
        expect(result.suggestion.length).toBeGreaterThan(0);
      }
    });
  }

  const ACCEPTABLE_TITLES = [
    'Q2 invoice intake',
    'Upload and Review Workflow',
    'Process payroll for November',
    'SAP Invoice Submission',
  ];

  for (const title of ACCEPTABLE_TITLES) {
    it(`accepts meaningful title "${title}"`, () => {
      const rendered = validRenderedSOP({ taskTitle: title });
      const result = validateRenderedSOP(rendered, validOutput());
      // Should not fail on generic_title (may pass or fail on another rule)
      if (!result.ok) {
        expect(result.reason).not.toBe('generic_title');
      }
    });
  }
});

// ─── Rule 6 (bonus): prose_only_purpose ──────────────────────────────────────

describe('validateRenderedSOP — Rule 6: prose_only_purpose', () => {
  it('rejects when whatThisIsFor starts with "This SOP describes "', () => {
    const rendered = validRenderedSOP({
      whatThisIsFor: 'This SOP describes the procedure for uploading workflow files.',
    });
    const result = validateRenderedSOP(rendered, validOutput());

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason).toBe('prose_only_purpose');
      expect(result.diagnostic.length).toBeGreaterThan(0);
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });

  it('is case-insensitive for the prose prefix', () => {
    const rendered = validRenderedSOP({
      whatThisIsFor: 'this sop describes how to upload invoices.',
    });
    const result = validateRenderedSOP(rendered, validOutput());

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason).toBe('prose_only_purpose');
    }
  });

  it('returns structured error with all fields non-empty', () => {
    const rendered = validRenderedSOP({
      whatThisIsFor: 'This SOP describes the invoice process.',
    });
    const result: SOPValidation = validateRenderedSOP(rendered, validOutput());

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason.length).toBeGreaterThan(0);
      expect(result.diagnostic.length).toBeGreaterThan(0);
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });
});

// ─── Rule ordering: first-match wins ─────────────────────────────────────────

describe('validateRenderedSOP — rule ordering', () => {
  it('banned_recorder_artifact wins when both banned artifact AND too_few_steps apply', () => {
    // 1 step (fails Rule 2) + banned string in action (fails Rule 1)
    const rendered = validRenderedSOP({
      steps: [operatorSOPStep({ number: 1, action: 'Click the div' })],
    });
    // Only 1 step in output.sop.steps — would also trigger too_few_steps
    const output = validOutput([{ ordinal: 1, stepId: 'step-1', title: 'Single step' }]);

    const result = validateRenderedSOP(rendered, output);

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason).toBe('banned_recorder_artifact');
    }
  });
});
