// ─── Rule 1: banned recorder artifacts ───────────────────────────────────────

describe('validateRenderedSOP — Rule 1: banned_recorder_artifact', () => {
  const BANNED_CASES: Array<{ label: string; banned: string }> = [
    { label: 'Click the div',          banned: 'Click the div' },
    { label: 'Click the span',         banned: 'Click the span' },
    { label: 'Click the svg',          banned: 'Click the svg' },
    { label: 'Click the p',            banned: 'Click the p' },
    { label: 'Click the li',           banned: 'Click the li' },
    { label: 'Click the section',      banned: 'Click the section' },
    { label: 'Interact with element',  banned: 'Interact with element' },
    { label: 'Perform action',         banned: 'Perform action' },
  ];

  for (const { label, banned } of BANNED_CASES) {
    it(`rejects "${label}" in rendered markdown`, () => {
      // Inject the banned string into the step action so it appears in rendered Markdown.
      const rendered = validRenderedSOP({
        steps: [
          operatorSOPStep({ number: 1, action: banned }),
          operatorSOPStep({ number: 2, action: 'Enter data' }),
          operatorSOPStep({ number: 3, action: 'Submit form' }),
        ],
      });
      const result = validateRenderedSOP(rendered, validOutput());

      expect(result.ok).toBe(false);
      if (!result.ok) {
        expect(result.reason).toBe('banned_recorder_artifact');
        expect(result.diagnostic).toContain(banned);
        expect(result.suggestion.length).toBeGreaterThan(0);
      }
    });
  }

  it('returns structured { ok, reason, diagnostic, suggestion } with all fields non-empty', () => {
    const rendered = validRenderedSOP({
      steps: [
        operatorSOPStep({ number: 1, action: 'Click the div' }),
        operatorSOPStep({ number: 2, action: 'Enter data' }),
      ],
    });
    const result: SOPValidation = validateRenderedSOP(rendered, validOutput());

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(typeof result.reason).toBe('string');
      expect(result.reason.length).toBeGreaterThan(0);
      expect(typeof result.diagnostic).toBe('string');
      expect(result.diagnostic.length).toBeGreaterThan(0);
      expect(typeof result.suggestion).toBe('string');
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });
});

// ─── Rule 2: too_few_steps ────────────────────────────────────────────────────

describe('validateRenderedSOP — Rule 2: too_few_steps', () => {
  it('rejects when output.sop.steps.length === 1', () => {
    const output = validOutput([{ ordinal: 1, stepId: 'step-1', title: 'Only step' }]);
    const result = validateRenderedSOP(validRenderedSOP(), output);

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason).toBe('too_few_steps');
      expect(result.diagnostic).toContain('1 step(s)');
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });

  it('rejects when output.sop.steps.length === 0', () => {
    const output = validOutput([]);
    const result = validateRenderedSOP(validRenderedSOP(), output);

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason).toBe('too_few_steps');
      expect(result.diagnostic).toContain('0 step(s)');
    }
  });

  it('returns structured error with all fields non-empty', () => {
    const output = validOutput([{ ordinal: 1, stepId: 'step-1', title: 'Only step' }]);
    const result: SOPValidation = validateRenderedSOP(validRenderedSOP(), output);

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason.length).toBeGreaterThan(0);
      expect(result.diagnostic.length).toBeGreaterThan(0);
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });
});

// ─── Rule 3: step_has_no_evidence ────────────────────────────────────────────

describe('validateRenderedSOP — Rule 3: step_has_no_evidence', () => {
  it('rejects when one step has instructions: []', () => {
    const output = validOutput([
      { ordinal: 1, stepId: 'step-1', title: 'Open form' },
      { ordinal: 2, stepId: 'step-2', title: 'Empty step', instructions: [] },
      { ordinal: 3, stepId: 'step-3', title: 'Submit' },
    ]);
    const result = validateRenderedSOP(validRenderedSOP(), output);

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason).toBe('step_has_no_evidence');
      expect(result.diagnostic).toContain('Step 2');
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });

  it('returns structured error with all fields non-empty', () => {
    const output = validOutput([
      { ordinal: 1, stepId: 'step-1', title: 'Open form' },
      { ordinal: 2, stepId: 'step-2', title: 'Empty step', instructions: [] },
    ]);

    // Step count is still 2 (passes Rule 2), so Rule 3 is reached.
    const result: SOPValidation = validateRenderedSOP(validRenderedSOP(), output);

    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.reason.length).toBeGreaterThan(0);
      expect(result.diagnostic.length).toBeGreaterThan(0);
      expect(result.suggestion.length).toBeGreaterThan(0);
    }
  });
});

